# Arrays
#
# - Arrays are stored in RAM.
# - RAM is measured in bytes, common for a computer to have gigabytes GB.
#   - 1 GB is 10^9
#   - 1 Byte is 8 bits.
#   - A bit is a position, 0b 0000 0001 = 1
# - Common for an integer to be stored in 4 bytes.
#   - 4 bytes is 32 bits.
# - RAM is a contiguous block of data.
#   - Data is stored at an address.
# - Arrays are always stored in contiguous memory.
# - Characters are stored in 1 byte/8 bits of memory.